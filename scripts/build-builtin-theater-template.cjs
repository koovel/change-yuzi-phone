const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const ROOT = process.cwd();
const OUTPUT_MODULE_RELATIVE_PATH = 'modules/phone-fusion/builtin-theater-template.js';
const OUTPUT_MODULE_PATH = path.join(ROOT, OUTPUT_MODULE_RELATIVE_PATH);
const SOURCE_DESCRIPTORS = Object.freeze([
    { role: 'theater', label: '小剧场2.1', relativePath: 'tables/generated/小剧场2.1.json' },
    { role: 'summary', label: '纪要', relativePath: 'tables/generated/纪要.json' },
]);
const BUILTIN_SOURCE_PATH = SOURCE_DESCRIPTORS.map((source) => source.relativePath).join(' + ');

function sha256(text) {
    return crypto.createHash('sha256').update(text).digest('hex');
}

function readSourceTemplate(source) {
    const absolutePath = path.join(ROOT, source.relativePath);
    const text = fs.readFileSync(absolutePath, 'utf8');
    return {
        ...source,
        text,
        template: JSON.parse(text),
        hash: sha256(text),
    };
}

function getSheetKeys(template) {
    return Object.keys(template || {}).filter((key) => key.startsWith('sheet_'));
}

function assertTemplateShape(source) {
    const { template, label } = source;
    if (!template || typeof template !== 'object' || Array.isArray(template)) {
        throw new Error(`${label} generated 模板必须是对象`);
    }
    if (template?.mate?.type !== 'chatSheets') {
        throw new Error(`${label} generated 模板 mate.type 必须是 chatSheets`);
    }
    if (getSheetKeys(template).length === 0) {
        throw new Error(`${label} generated 模板必须包含至少一个 sheet_*`);
    }
}

function assertSameMate(primary, secondary) {
    if (JSON.stringify(primary.template.mate) !== JSON.stringify(secondary.template.mate)) {
        throw new Error(`${primary.label} 与 ${secondary.label} 的 mate 不一致，禁止 silent override`);
    }
}


function assertNoSheetConflicts(sources) {
    const seenKeys = new Map();
    const seenNames = new Map();

    for (const source of sources) {
        for (const key of getSheetKeys(source.template)) {
            if (seenKeys.has(key)) {
                throw new Error(`sheet key 冲突：${key} 同时存在于 ${seenKeys.get(key)} 与 ${source.label}`);
            }
            seenKeys.set(key, source.label);

            const name = String(source.template[key]?.name || '').trim();
            if (!name) {
                throw new Error(`${source.label} 的 ${key} 缺少 sheet name`);
            }
            if (seenNames.has(name)) {
                throw new Error(`sheet name 冲突：${name} 同时存在于 ${seenNames.get(name)} 与 ${source.label}`);
            }
            seenNames.set(name, source.label);
        }
    }
}

function cloneJson(value) {
    return JSON.parse(JSON.stringify(value));
}

function buildMergedTemplate(sources) {
    const [theater, summary] = sources;
    assertSameMate(theater, summary);
    assertNoSheetConflicts(sources);

    const merged = { mate: cloneJson(theater.template.mate) };
    const theaterSheetKeys = getSheetKeys(theater.template);
    for (const key of theaterSheetKeys) {
        merged[key] = cloneJson(theater.template[key]);
    }

    const maxOrderNo = theaterSheetKeys.reduce((max, key) => {
        const orderNo = theater.template[key]?.orderNo;
        return Number.isFinite(orderNo) ? Math.max(max, orderNo) : max;
    }, -1);
    getSheetKeys(summary.template).forEach((key, index) => {
        merged[key] = cloneJson(summary.template[key]);
        merged[key].orderNo = maxOrderNo + index + 1;
    });

    return merged;
}

function createCompositeHash(sources, template) {
    const payload = {
        sources: sources.map((source) => ({ path: source.relativePath, sha256: source.hash })),
        templateSha256: sha256(JSON.stringify(template)),
    };
    return sha256(JSON.stringify(payload));
}

function formatModule({ template, sources, hash }) {
    const serializedTemplate = JSON.stringify(template, null, 4);
    const sourcePaths = sources.map((source) => source.relativePath);
    const sourceHashes = Object.fromEntries(sources.map((source) => [source.relativePath, source.hash]));
    return [
        '// This file is generated by scripts/build-builtin-theater-template.cjs.',
        `// Sources: ${sourcePaths.join(' + ')}`,
        '// Do not edit manually.',
        '',
        `export const BUILTIN_THEATER_TEMPLATE_SOURCE_PATH = '${BUILTIN_SOURCE_PATH}';`,
        `export const BUILTIN_THEATER_TEMPLATE_SOURCE_PATHS = ${JSON.stringify(sourcePaths, null, 4)};`,
        `export const BUILTIN_THEATER_TEMPLATE_SOURCE_HASHES = ${JSON.stringify(sourceHashes, null, 4)};`,
        `export const BUILTIN_THEATER_TEMPLATE_SHA256 = '${hash}';`,
        `const BUILTIN_THEATER_TEMPLATE_DATA = ${serializedTemplate};`,
        '',
        'export const BUILTIN_THEATER_TEMPLATE = Object.freeze(BUILTIN_THEATER_TEMPLATE_DATA);',
        '',
        'export function createBuiltinTheaterTemplate() {',
        '    return JSON.parse(JSON.stringify(BUILTIN_THEATER_TEMPLATE_DATA));',
        '}',
        '',
    ].join('\n');
}

function main() {
    const sources = SOURCE_DESCRIPTORS.map(readSourceTemplate);
    sources.forEach(assertTemplateShape);
    const template = buildMergedTemplate(sources);
    const hash = createCompositeHash(sources, template);
    const moduleText = formatModule({ template, sources, hash });
    fs.mkdirSync(path.dirname(OUTPUT_MODULE_PATH), { recursive: true });
    fs.writeFileSync(OUTPUT_MODULE_PATH, moduleText, 'utf8');
    const sheetCount = getSheetKeys(template).length;
    console.log(`[build-builtin-theater-template] 已生成 ${OUTPUT_MODULE_RELATIVE_PATH}，source ${sources.length} 个，sheet ${sheetCount} 个，sha256=${hash}`);
}

try {
    main();
} catch (error) {
    console.error(`[build-builtin-theater-template] ${error.message}`);
    process.exitCode = 1;
}
