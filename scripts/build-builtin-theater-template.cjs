const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const ROOT = process.cwd();
const SOURCE_JSON_RELATIVE_PATH = 'tables/generated/小剧场2.1.json';
const OUTPUT_MODULE_RELATIVE_PATH = 'modules/phone-fusion/builtin-theater-template.js';
const SOURCE_JSON_PATH = path.join(ROOT, SOURCE_JSON_RELATIVE_PATH);
const OUTPUT_MODULE_PATH = path.join(ROOT, OUTPUT_MODULE_RELATIVE_PATH);

function readSourceTemplate() {
    const text = fs.readFileSync(SOURCE_JSON_PATH, 'utf8');
    return {
        text,
        template: JSON.parse(text),
        hash: crypto.createHash('sha256').update(text).digest('hex'),
    };
}

function assertTemplateShape(template) {
    if (!template || typeof template !== 'object' || Array.isArray(template)) {
        throw new Error('小剧场 generated 模板必须是对象');
    }
    if (template?.mate?.type !== 'chatSheets') {
        throw new Error('小剧场 generated 模板 mate.type 必须是 chatSheets');
    }
    const sheetKeys = Object.keys(template).filter((key) => key.startsWith('sheet_'));
    if (sheetKeys.length === 0) {
        throw new Error('小剧场 generated 模板必须包含至少一个 sheet_*');
    }
}

function formatModule({ template, hash }) {
    const serializedTemplate = JSON.stringify(template, null, 4);
    return [
        '// This file is generated by scripts/build-builtin-theater-template.cjs.',
        `// Source: ${SOURCE_JSON_RELATIVE_PATH}`,
        '// Do not edit manually.',
        '',
        `export const BUILTIN_THEATER_TEMPLATE_SOURCE_PATH = '${SOURCE_JSON_RELATIVE_PATH}';`,
        `export const BUILTIN_THEATER_TEMPLATE_SHA256 = '${hash}';`,
        `const BUILTIN_THEATER_TEMPLATE_DATA = ${serializedTemplate};`,
        '',
        'export const BUILTIN_THEATER_TEMPLATE = Object.freeze(BUILTIN_THEATER_TEMPLATE_DATA);',
        '',
        'export function createBuiltinTheaterTemplate() {',
        '    return JSON.parse(JSON.stringify(BUILTIN_THEATER_TEMPLATE_DATA));',
        '}',
        '',
    ].join('\n');
}

function main() {
    const source = readSourceTemplate();
    assertTemplateShape(source.template);
    const moduleText = formatModule(source);
    fs.mkdirSync(path.dirname(OUTPUT_MODULE_PATH), { recursive: true });
    fs.writeFileSync(OUTPUT_MODULE_PATH, moduleText, 'utf8');
    const sheetCount = Object.keys(source.template).filter((key) => key.startsWith('sheet_')).length;
    console.log(`[build-builtin-theater-template] 已生成 ${OUTPUT_MODULE_RELATIVE_PATH}，sheet ${sheetCount} 个，sha256=${source.hash}`);
}

try {
    main();
} catch (error) {
    console.error(`[build-builtin-theater-template] ${error.message}`);
    process.exitCode = 1;
}
